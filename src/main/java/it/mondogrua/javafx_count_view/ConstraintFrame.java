package it.mondogrua.javafx_count_view;

public class ConstraintFrame {

    private int x;
    private int y;

    public ConstraintFrame(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

}
